CREATE OR REPLACE PROCEDURE pr_insert_account (
    p_email     IN VARCHAR2,
    p_password  IN VARCHAR2,
    p_role      IN VARCHAR2,
    p_active    IN NUMBER DEFAULT 1
) AS
    ex_invalid_role EXCEPTION;
    PRAGMA EXCEPTION_INIT(ex_invalid_role, -2290);
    v_msg VARCHAR2(4000);
    v_code NUMBER;
BEGIN
    IF p_role NOT IN ('ADMIN', 'RESPONSIBLE', 'VETERINARIAN') THEN
        RAISE ex_invalid_role;
    END IF;

    INSERT INTO T_ACCOUNT (email, password, role, active)
    VALUES (p_email, p_password, p_role, p_active);
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        v_code := SQLCODE;
        v_msg  := SUBSTR(SQLERRM || ' [Email: '|| p_email ||']', 1, 3900);
        INSERT INTO T_LOG_ERROS_PG (nome_proc, usuario_bd, codigo_erro, mensagem_erro)
        VALUES ('PR_INSERT_ACCOUNT', sys_context('USERENV', 'CURRENT_USER'), v_code, v_msg);
        COMMIT;
END pr_insert_account;
/

