-- CURSOR EXPLICITO 1: Subtotais de Consultas por Status
DECLARE
    CURSOR c_consultas IS
        SELECT id, status, reason
        FROM T_APPOINTMENT
        ORDER BY status, id;

    v_status_anterior T_APPOINTMENT.status%TYPE := NULL;
    v_qtd_status      NUMBER := 0;
    v_total           NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('===== CURSOR 1: RELATÓRIO SUBSTOTALIZADO DE CONSULTAS =====');

    FOR consulta IN c_consultas LOOP
        IF v_status_anterior IS NOT NULL AND v_status_anterior <> consulta.status THEN
            DBMS_OUTPUT.PUT_LINE('>> Subtotal do status [' || v_status_anterior || ']: ' || v_qtd_status);
            DBMS_OUTPUT.PUT_LINE('---------------------------------------------------------');
            v_qtd_status := 0;
        END IF;

        v_status_anterior := consulta.status;
        DBMS_OUTPUT.PUT_LINE('Consulta ID: ' || consulta.id || ' | Status: ' || consulta.status || ' | Motivo: ' || consulta.reason);
        
        v_qtd_status := v_qtd_status + 1;
        v_total      := v_total + 1;
    END LOOP;

    IF v_qtd_status > 0 THEN
        DBMS_OUTPUT.PUT_LINE('>> Subtotal do status [' || v_status_anterior || ']: ' || v_qtd_status);
    END IF;

    DBMS_OUTPUT.PUT_LINE('=========================================================');
    DBMS_OUTPUT.PUT_LINE('TOTAL GERAL DE CONSULTAS PROCESSADAS: ' || v_total);
END;
/

-- CURSOR EXPLICITO 2: Agregados Aritméticos de Histórico Clínico (AVG, COUNT) por Pet
DECLARE
    CURSOR c_historico IS
        SELECT p.name AS pet_name,
               ROUND(AVG(ch.temperature), 2) AS media_temperatura,
               ROUND(AVG(ch.heartRate), 2) AS media_batimentos,
               COUNT(ch.id) AS total_medicoes
        FROM T_PET p
        INNER JOIN T_CLINICALHISTORY ch ON ch.pet_id = p.id
        GROUP BY p.name
        ORDER BY p.name;
BEGIN
    DBMS_OUTPUT.PUT_LINE('===== CURSOR 2: AGREGADOS DE SAÚDE MÉDIA POR PACIENTE =====');
    FOR reg IN c_historico LOOP
        DBMS_OUTPUT.PUT_LINE('Pet: ' || RPAD(reg.pet_name, 12) || 
                             ' | Média Temp: ' || reg.media_temperatura || '°C' ||
                             ' | Média Batimentos: ' || LPAD(reg.media_batimentos, 6) || ' bpm' ||
                             ' | Total Medições: ' || reg.total_medicoes);
    END LOOP;
END;
/

-- CURSOR EXPLICITO 3: Triagem Operacional de Pets com Tomada de Decisão
DECLARE
    CURSOR c_triagem IS
        SELECT name, currentStatus
        FROM T_PET
        ORDER BY currentStatus DESC;
    v_criticos NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('===== CURSOR 3: TRIAGEM OPERACIONAL DE PACIENTES =====');
    FOR pet IN c_triagem LOOP
        IF pet.currentStatus = 'RISK' THEN
            DBMS_OUTPUT.PUT_LINE('[ALERTA CRÍTICO] O paciente ' || pet.name || ' encontra-se em estado de RISCO!');
            v_criticos := v_criticos + 1;
        ELSIF pet.currentStatus = 'ATTENTION' THEN
            DBMS_OUTPUT.PUT_LINE('[ATENÇÃO] O paciente ' || pet.name || ' necessita de acompanhamento contínuo.');
        ELSE
            DBMS_OUTPUT.PUT_LINE('[ESTÁVEL] O paciente ' || pet.name || ' apresenta parâmetros estáveis.');
        END IF;
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('---------------------------------------------------------');
    DBMS_OUTPUT.PUT_LINE('Total de pacientes críticos em atenção imediata: ' || v_criticos);
END;
/

-- CURSOR EXPLICITO 4: Varredura de Auditoria de Logs de Erro Gerados
DECLARE
    CURSOR c_logs IS
        SELECT nome_proc, codigo_erro, mensagem_erro
        FROM T_LOG_ERROS_PG
        ORDER BY id_log;
    v_qtd NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('===== CURSOR 4: AUDITORIA INTERNA DO SISTEMA (LOGS) =====');
    FOR erro IN c_logs LOOP
        v_qtd := v_qtd + 1;
        DBMS_OUTPUT.PUT_LINE('Falha #' || v_qtd || ' | Routine: ' || erro.nome_proc || 
                             ' | Oracle Error: ORA-' || LPAD(ABS(erro.codigo_erro), 5, '0') || 
                             ' | Detalhe: ' || SUBSTR(erro.mensagem_erro, 1, 65));
    END LOOP;
END;
/


--- =====================================================================
--- 7. SEÇÃO DE BLOCOS DE ANÁLISE TEMPORAL (FUNÇÃO ANALÍTICA - LAG/LEAD)
--- =====================================================================
DECLARE
    CURSOR c_analitico IS
        SELECT pet_id AS id,
               TO_CHAR(recordedAt, 'DD/MM/YYYY HH24:MI') AS dt,
               NVL(TO_CHAR(LAG(temperature, 1) OVER (PARTITION BY pet_id ORDER BY recordedAt)), 'Início') AS ant,
               TO_CHAR(temperature) AS atual,
               NVL(TO_CHAR(LEAD(temperature, 1) OVER (PARTITION BY pet_id ORDER BY recordedAt)), 'Final') AS prox
        FROM T_CLINICALHISTORY;
BEGIN
    DBMS_OUTPUT.PUT_LINE('===== SEÇÃO 7: MONITORAMENTO ANALÍTICO DE SENSOR (LAG/LEAD) =====');
    FOR r IN c_analitico LOOP
        DBMS_OUTPUT.PUT_LINE('ID Pet: ' || r.id || ' | Data/Hora: ' || r.dt || 
                             ' | Métrica Anterior: ' || RPAD(r.ant, 6) || 
                             ' | Leitura Atual: ' || RPAD(r.atual, 5) || 
                             ' | Próxima Leitura: ' || r.prox);
    END LOOP;
END;
/

